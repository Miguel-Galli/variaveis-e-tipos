public class Livro {
    String titulo;
    String autor;
    int numeroPaginas;
    double preco;
    char categoriaLivro;

    public Livro(String titulo, String autor, int numeroPaginas, double preco, char categoriaLivro) {
        this.titulo = titulo;
        this.autor = autor;
        this.numeroPaginas = numeroPaginas;
        this.preco = preco;
        this.categoriaLivro = categoriaLivro;
    }

}
